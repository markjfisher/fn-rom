BBC_MAP = {
    "┌": "+",
    "┐": "+",
    "└": "+",
    "┘": "+",
    "┤": "+",
    "├": "+",
    "┬": "+",
    "┴": "+",
    "┼": "+",
    "─": "-",
    "│": "|",
    "▌": "#",
    "▐": "#",
    "▄": "#",
    "▀": "#",
    "▖": ".",
    "▗": ".",
    "▘": ".",
    "▝": ".",
    "▜": "#",
    "▛": "#",
    "▟": "#",
    "▙": "#",
    "█": "#",
}


def to_bbc_chars(text: str) -> str:
    return "".join(BBC_MAP.get(ch, ch) for ch in text)


def read_shape(shape_id: int, name: str) -> tuple[int, int, str]:
    path = SHAPES_DIR / name
    lines = path.read_text(encoding="utf-8").splitlines()
    shape_text = "".join(lines[1:])
    width = isqrt(len(shape_text))
    if width * width != len(shape_text):
        raise ValueError(f"{name}: invalid square size")
    return shape_id, width, to_bbc_chars(shape_text)


def bbc_string_literal(text: str) -> str:
    return '"' + text.replace('"', "'") + '"'


def main() -> None:
    shapes = [read_shape(i, name) for i, name in enumerate(SHAPE_FILES)]
    total_payload = 0

    print("REM Generated by tools/generate_bbc_shapes.py")
    print(f"shapeCount%={len(shapes)}")
    for shape_id, width, payload in shapes:
        print(f"DATA {shape_id},{width},{len(payload)},{bbc_string_literal(payload)}")
        total_payload += len(payload)

    print(f"REM total size: {total_payload}")

if __name__ == "__main__":
    main()
